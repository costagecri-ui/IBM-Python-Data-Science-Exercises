##EXPRESSIONS##
#I added the print() function in every line, but these operations can also be executed without it
# Addition operation expression
print(43 + 60 + 16 + 41)

#Difference operation expression
print(50 - 60)
# Multiplication operation expression

print(5 * 5)
# Division operation expression

print(25 / 5)
# Division operation; in this case, the result is a float.
print(25 / 6)
#if we want an integer we can use //

print(25//6)
#mathematical expressions

print((30 + 2) * 60)
##VARIABLES##
# Store value into variable

x = 43 + 60 + 16 + 41
# Print out the value in variable

print(x)
#Using a new variable to store the result of the operation.

y = x / 60

print(y)
# you can also use the variables within expressions
z = x + y
print(z)
